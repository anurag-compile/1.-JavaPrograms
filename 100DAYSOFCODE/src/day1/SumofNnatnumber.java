package day1;

import java.util.Scanner;

public class SumofNnatnumber {
    public static void main(String[] args) {
        int n;
        System.out.println("Enter No. of Term: ");
        Scanner in = new Scanner(System.in);
        n = in.nextInt();

        for (int i =1; i <= n; i++)
        {
            System.out.println(i + " ");
        }
    }
}
